#!/bin/sh

RED='\033[0;31m'
NC='\033[0m'
BLUE='\033[0;94m'
#############################################################################################
##                                 ECHO LINE SEPARATOR
#############################################################################################
ScreenUtils.separateLine()
{
    echo -e "${BLUE}------------------------------------------------------------------------------------------------------------------------${NC}"
}

#############################################################################################
##                                 ECHO BANNER
#############################################################################################
ScreenUtils.echoBanner()
{
    echo ""
    echo ""
    echo ""
    echo ""
    echo -e "${BLUE}#########################################################################################################################"
    echo -e "##                                 $1"
    echo -e "#########################################################################################################################${NC}"
}


#############################################################################################
##                                 ECHO ERROR
#############################################################################################
ScreenUtils.echoError()
{
    >&2 echo -e "${RED}[ERROR] $( IFS=$"<"; echo "${FUNCNAME[*]}") \n $1${NC}"
    exit 1
}