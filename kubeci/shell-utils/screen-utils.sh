#!/bin/sh

RED='\033[0;31m'
YELLOW='\033[33m'
NC='\033[0m'
BLUE='\033[0;94m'
GREEN='\033[32m'
#############################################################################################
##                                 ECHO LINE SEPARATOR
#############################################################################################
ScreenUtils.separateLine()
{
    echo -e "${BLUE}------------------------------------------------------------------------------------------------------------------------${NC}"
}

#############################################################################################
##                                 ECHO WARNING
#############################################################################################
ScreenUtils.echoWarning()
{
    echo -e "${YELLOW}$1${NC}"
}

#############################################################################################
##                                 ECHO SUCCESS
#############################################################################################
ScreenUtils.echoSuccess()
{
    echo -e "${GREEN}$1${NC}"
}

#############################################################################################
##                                 ECHO IMPORTANT
#############################################################################################
ScreenUtils.echoImportant()
{
    echo -e "${BLUE}$1${NC}"
}

#############################################################################################
##                                 ECHO BANNER
#############################################################################################
ScreenUtils.echoBanner()
{
    echo ""
    echo ""
    echo ""
    echo ""
    echo -e "${BLUE}#########################################################################################################################"
    echo -e "##                                 $1"
    echo -e "#########################################################################################################################${NC}"
}


#############################################################################################
##                                 ECHO ERROR
#############################################################################################
ScreenUtils.echoError()
{
    >&2 echo -e "${RED}[ERROR] $( IFS=$"<"; echo "${FUNCNAME[*]}") \n $1${NC}"
    exit 1
}